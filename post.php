						<div class="post-content module">
							<div class="summary module">
								<p>Studying an animal’s behavior can be a real pain for researchers. It takes hours of observing an animal in carefully-designed lab environments, or out in the wild, to glean an ounce of insight into how they act and why. For a team of biologists at Columbia University, there seemed to be a better way: set up a camera and let artificial intelligence do the tedious observing.</p>
								<p>The researchers recently <a href="https://elifesciences.org/articles/32605/figures" target="_blank">published a study </a>about their technique in the journal&nbsp;<em>eLife.&nbsp;</em></p>
								<p>The organism in question is a&nbsp;<em>Hydra vulgaris</em>, a tiny animal related to jellyfish and coral that tends to live in ponds. In the past, the researchers studied the animal’s nervous system and figured out which parts of its brain prompt much of its behavior. Now they’re using AI to track all of that behavior in hopes of figuring out how the two pair up.</p>
								<p>The team used an artificial intelligence algorithm that automatically annotated the behaviors of the wormlike <em>Hydra&nbsp;</em><span class="s1">—&nbsp;</span>all of its&nbsp;wiggles, pivots, stretches, and bends. They learned that the species only really engaged in <a href="https://elifesciences.org/articles/32605#abstract" target="_blank">six basic behaviors</a>, which it did no matter the environmental conditions, such as the amount of light, the temperature, or the amount of food nearby.</p>
